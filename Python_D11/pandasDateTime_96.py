import datetime
from datetime import datetime

print("a)", datetime(2012, 1, 15))

print("b)", datetime(2011, 1, 11, 21, 20))

print("c)", datetime.now())

print("d)", datetime.date(datetime(2012, 5, 22)))

print("e)", datetime.now().date())

print("f)", datetime.time(datetime(2012, 12, 15, 18, 12)))

print("g)", datetime.now().time())
