import json
data = {"name": "Siva", "age": 22, "city": "Hyderabad"}
a = json.dumps(data)
print("1:", a)
b = json.loads(a)
print("2:", b)
f = open("data.json", "w")
json.dump(data, f)
f.close()
print("3:", "file created")
f = open("data.json", "r")
c = json.load(f)
print("4:", c)
f.close()
d = json.dumps(data, indent=4)
print("5:", d)
e = json.dumps(data, sort_keys=True)
print("6:", e)
enc = json.JSONEncoder()
x = enc.encode({"a": 1, "b": 2})
print("7:", x)
dec = json.JSONDecoder()
y = dec.decode('{"a": 10, "b": 20}')
print("8:", y)

