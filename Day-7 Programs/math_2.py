import math
num = float(input("Enter the number: "))
num1 = 5
num2 = 10
# print square root
print(math.sqrt(num))
# print ceil number
print(math.ceil(num))
# print floor number
print(math.floor(num))
# print pi value
print(math.pi)
# print factorial
print(math.factorial(num2))
# print gcd
print(math.gcd(num1, num2))
# print expo
print(math.exp(num))
# print trig-no values
print(math.sin(num))
print(math.cos(num))
print(math.tan(num))




