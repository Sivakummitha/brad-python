import math as ma
pi = 3.14
a = -6.26
# rounds the value
print(round(pi))
# Returns the smallest integer greater than or equal to pi
print(ma.ceil(pi))
# Returns the largest integer less than or equal to pi
print(ma.floor(pi))
# power function
print(pow(pi,2))
# square root
print(ma.sqrt(pi))
# absolute value(it converts negative to positive)
print(abs(a))

# max function & min function
x = 1
y = 2
z = 3
print(max(x,y,z))
print(min(x,y,z))
